public class LoopsDemo {
    public static void main(String[] args) {

        // for loop
        for (int i = 1; i <= 5; i++) {
            System.out.println("For loop: " + i);
        }

        // while loop
        int j = 1;
        while (j <= 5) {
            System.out.println("While loop: " + j);
            j++;
        }
    }
}
